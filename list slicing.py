L=[2,34,45,56,66,3,31,10]

print(L[:])#[2, 34, 45, 56, 66, 3, 31, 10]
print(L[::])#[2, 34, 45, 56, 66, 3, 31, 10]
print(L[::-1])#[10, 31, 3, 66, 56, 45, 34, 2]
print(L[::1])#[2, 34, 45, 56, 66, 3, 31, 10]
print(L[::2])#[2, 45, 66, 31]
print(L[::3])#[2, 56, 31]


print(L[4:])#[66, 3, 31, 10]
print(L[:4])#[2, 34, 45, 56]
print(L[3:6])#[56, 66, 3]
print(L[2:7:2])#[45, 66, 31]

print(L[-7:-4])#[34, 45, 56]
print(L[-7:-2:2])#[34, 56, 3]
print(L[-7:-2:-2])#[]

